#include "libft.h"

t_form   ft_is_stick(char *str, t_form form)
{
	int i;

	i = 0;
	while (str[i] && str[i] != '#')
		i++;
	if (str[i + 1] == '#' && str[i + 2] == '#' && str[i + 3] == '#')
		form.su++;
	if (str[i + 4] == '#' && str[i + 8] == '#' && str[i + 12] == '#')
		form.sd++;
    return (form);
}
