#include "libft.h"

void   ft_is_square(char *str, t_list *list)
{
	int i;

	i = 0;
	while (str[i] != '#' && str[i])
		i++;
	if (str[i + 1] == '#' && str[i + 5] == '#' && str[i + 6] == '#')
		ft_list_push_back(list, "square");
}
